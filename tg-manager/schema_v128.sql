-- v128: хранить целевой список получателей рассылки для корректного resume
-- после перезапуска процесса.
--
-- broadcaster.run умеет докатывать рассылку (delivery log + skip already_sent),
-- но при рестарте процесса задача умирала и никто не перезапускал рассылки в
-- статусе 'running' — они зависали навсегда. Чтобы resume сегментной рассылки
-- не разослал её всей аудитории, сохраняем явный список получателей.
-- NULL = рассылка по полной аудитории бота (resume возьмёт полную аудиторию).

ALTER TABLE broadcasts
    ADD COLUMN IF NOT EXISTS target_user_ids JSONB;
