def get_presets(asset_type: str) -> list[dict]:
    """Return presets list for given asset_type."""
    return PRESETS.get(asset_type, [])


def get_preset(asset_type: str, preset_id: str) -> dict | None:
    """Return a single preset by asset_type and id."""
    for p in PRESETS.get(asset_type, []):
        if p["id"] == preset_id:
            return p
    return None


# Flat mapping for O(1) lookup  (separator __ avoids aiogram3 CallbackData clash)
_ALL: dict[str, dict] = {
    f"{atype}__{p['id']}": p
    for atype, plist in PRESETS.items()
    for p in plist
}


def get_preset_by_key(key: str) -> dict | None:
    """Return preset by combined key 'asset_type__preset_id'."""
    return _ALL.get(key)
